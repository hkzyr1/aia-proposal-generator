def get_premium_paid(year: int, annual_premium: int = 10000) -> int:
    """计算已缴保费总额"""
    base = year * 10000 if year <= 5 else 50000
    scale = annual_premium / 10000
    return round(base * scale)


def generate_benefit_table(annual_premium: int, key_years: list[int]) -> list[dict]:
    """
    根据客户年缴保费，生成关键年份收益数据
    """
    scale = annual_premium / 10000
    result = []
    for y in key_years:
        total = round(BASE_TOTAL_SURRENDER[y] * scale)
        guaranteed = round(BASE_GUARANTEED_CV[y] * scale)
        non_guaranteed = total - guaranteed
        premium = get_premium_paid(y, annual_premium)
        result.append({
            "year": y,
            "premium_paid": premium,
            "guaranteed_cv": guaranteed,
            "non_guaranteed_bonus": non_guaranteed,
            "total_value": total,
        })
    return result


def simulate_withdrawal(annual_premium: int, start_year: int, end_year: int = 100) -> dict:
    """
    计算固定提取后的年度余额模拟
    提取金额 = 开始提取年份的预期总价值 × 6.5%
    """
    scale = annual_premium / 10000
    start_value = round(BASE_TOTAL_SURRENDER[start_year] * scale)
    annual_withdrawal = round(start_value * 0.065)

    balance = start_value
    projections = []

    for y in range(start_year, end_year + 1):
        if y > start_year:
            growth_rate = (BASE_TOTAL_SURRENDER[y] - BASE_TOTAL_SURRENDER[y - 1]) / BASE_TOTAL_SURRENDER[y - 1]
            balance = balance * (1 + growth_rate)
        balance -= annual_withdrawal
        projections.append({"year": y, "balance": round(balance)})

    return {
        "annual_withdrawal": annual_withdrawal,
        "start_value": start_value,
        "projections": projections,
    }


def format_usd(amount: int) -> str:
    """格式化美元金额，添加千分位逗号"""
    return f"{amount:,}"
